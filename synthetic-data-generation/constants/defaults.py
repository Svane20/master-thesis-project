class ImageDefaults:
    """
    Default values for image settings.

    Attributes:
        SIZE (int): The default image size in pixels (both width and height).
        WIDTH (int): The default width of the image.
        HEIGHT (int): The default height of the image.
    """
    SIZE: int = 2048
    WIDTH: int = 2048
    HEIGHT: int = 2048


class WorldDefaults:
    """
    Default values for world settings.

    Attributes:
        SIZE (float): The default size of the world.
    """
    SIZE: float = 100.0
